package demo4;

public class SwitchCase {
	public static void main(String[] args)
	{
		String n = "abc";
		switch(n)
		{
		case "abc":
			System.out.println("One");
			break;
		case "def":
			System.out.println("Two");
			break;
		case "ghi":
			System.out.println("Three");
			break;
		case "jkl":
			System.out.println("Four");
			break;
		case "mno":
			System.out.println("Five");
			break;
		case "pqr":
			System.out.println("Six");
			break;
		}
	
	}

}
